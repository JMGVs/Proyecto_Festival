    private void cargarUsuarios() {
        DefaultTableModel model = (DefaultTableModel) tblUsuarios.getModel();
        model.setRowCount(0);
        
        List<Entity.Usuario> usuarios = usuarioController.findUsuarioEntities();
        for (Entity.Usuario usuario : usuarios) {
            Object[] row = {
                usuario.getIdUsuario(),
                usuario.getNombreU(),
                usuario.getIdRol().getDescripcion()
            };
            model.addRow(row);
        }
    }
    
    private void cargarFestivales() {
        DefaultTableModel model = (DefaultTableModel) tblFestival.getModel();
        model.setRowCount(0);
        
        List<Entity.Festival> festivales = festivalController.findFestivalEntities();
        for (Entity.Festival festival : festivales) {
            Object[] row = {
                festival.getIdFestival(),
                festival.getNombre(),
                festival.getFechaInicio(),
                festival.getFechaFinal()
            };
            model.addRow(row);
        }
    }
    
    private void cargarEscenarios() {
        DefaultTableModel model = (DefaultTableModel) tblEscenarios.getModel();
        model.setRowCount(0);
        
        List<Entity.Escenario> escenarios = escenarioController.findEscenarioEntities();
        for (Entity.Escenario escenario : escenarios) {
            Object[] row = {
                escenario.getIdEscenario(),
                escenario.getNombre(),
                escenario.getCapacidad(),
                escenario.getUbicacion()
            };
            model.addRow(row);
        }
    }
    
    private void cargarPresentaciones() {
        DefaultTableModel model = (DefaultTableModel) tblPresentaciones.getModel();
        model.setRowCount(0);
        
        List<Entity.Presentacion> presentaciones = presentacionController.findPresentacionEntities();
        for (Entity.Presentacion presentacion : presentaciones) {
            Object[] row = {
                presentacion.getIdPresentacion(),
                presentacion.getTitulo(),
                presentacion.getDuracion(),
                presentacion.getCostoBase(),
                presentacion.getIdEscenario().getNombre(),
                presentacion.getIdFestival().getNombre()
            };
            model.addRow(row);
        }
    }

