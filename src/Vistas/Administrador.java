    private void btnEliminarFestivalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarFestivalActionPerformed
        int filaSeleccionada = tblFestival.getSelectedRow();
        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione un festival para eliminar");
            return;
        }
        
        int idFestival = (int) tblFestival.getValueAt(filaSeleccionada, 0);
        try {
            festivalController.destroy(idFestival);
            cargarFestivales();
            JOptionPane.showMessageDialog(this, "Festival eliminado correctamente");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al eliminar festival: " + e.getMessage());
        }
    }//GEN-LAST:event_btnEliminarFestivalActionPerformed

