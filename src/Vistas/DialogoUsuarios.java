public void llenarComboTurnos() {
    try {
        List<Rol> lisrol = jpaRol.findRolEntities();
        cbRol.removeAllItems();
        for (Rol rol : lisrol) {
            cbRol.addItem(rol); 
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Error al llenar Datos: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
}


