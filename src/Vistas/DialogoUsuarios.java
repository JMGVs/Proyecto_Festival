    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try {
            if (txtUsuario.getText().isEmpty() || txtContrasena.getPassword().length == 0) {
                JOptionPane.showMessageDialog(this, "El nombre de usuario y la contraseña no pueden estar vacíos");
                return;
            }

            Usuario usuario = new Usuario();
            usuario.setNombreU(txtUsuario.getText());
            usuario.setContrasena(new String(txtContrasena.getPassword()));
            usuario.setIdRol((Rol) cbRol.getSelectedItem());

            jpaUsuario.create(usuario);

            JOptionPane.showMessageDialog(this, "Usuario agregado correctamente");
            dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al agregar usuario: " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

