    private void btnEditarPresentacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarPresentacionActionPerformed
        int filaSeleccionada = tblPresentaciones.getSelectedRow();
        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione una presentación para editar");
            return;
        }
        
        int idPresentacion = (int) tblPresentaciones.getValueAt(filaSeleccionada, 0);
        Presentacion presentacion = presentacionController.findPresentacion(idPresentacion);

        DialogoPresentacion dialogo = new DialogoPresentacion(this, true, presentacion);
        dialogo.setVisible(true);
        cargarPresentaciones();
    }//GEN-LAST:event_btnEditarPresentacionActionPerformed

