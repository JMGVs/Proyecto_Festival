    private void cargarPresentaciones() {
        DefaultTableModel model = (DefaultTableModel) tblPresentaciones.getModel();
        model.setRowCount(0);
        
        List<Presentacion> presentaciones = presentacionController.findPresentacionEntities();
        for (Presentacion presentacion : presentaciones) {
            String tipo = "";
            double costoFinal = presentacion.getCostoBase();
            
            if (conciertoController.findConcierto(presentacion.getIdPresentacion()) != null) {
                tipo = "Concierto";
                costoFinal = presentacion.getCostoBase() * 
                    conciertoController.findConcierto(presentacion.getIdPresentacion()).getNumArtistas();
            } else if (obraTeatroController.findObrateatro(presentacion.getIdPresentacion()) != null) {
                tipo = "Obra de Teatro";
                costoFinal = presentacion.getCostoBase() * presentacion.getDuracion();
            } else if (exposicionArteController.findExposicionarte(presentacion.getIdPresentacion()) != null) {
                tipo = "Exposición de Arte";
                costoFinal = presentacion.getCostoBase() * 
                    exposicionArteController.findExposicionarte(presentacion.getIdPresentacion()).getDiasExposicion();
            }
            
            Object[] row = {
                presentacion.getIdPresentacion(),
                presentacion.getTitulo(),
                tipo,
                presentacion.getDuracion() + " min",
                "$" + presentacion.getCostoBase(),
                "$" + costoFinal,
                presentacion.getIdEscenario().getNombre(),
                presentacion.getIdFestival().getNombre(),
                presentacion.getIdEscenario().getCapacidad() + " personas"
            };
            model.addRow(row);
        }
    }
