  private void llenarDatosPresentacion() {
        presentacion.setTitulo(txtTitulo.getText());
        presentacion.setDuracion(Integer.parseInt(txtDuracion.getText()));
        presentacion.setCostoBase(Double.parseDouble(txtCostoBase.getText()));
        presentacion.setIdEscenario((Escenario) cbEscenario.getSelectedItem());
        presentacion.setIdFestival((Festival) cbFestival.getSelectedItem());
    }

private void guardarPresentacion() throws Exception {
        if (presentacion.getIdPresentacion() == null) {
            presentacionController.create(presentacion);
            presentacion = presentacionController.findPresentacion(presentacion.getIdPresentacion());
        } else {
            presentacionController.edit(presentacion);
        }
    }

 private void guardarTipoPresentacion() throws Exception {
        String tipo = (String) cbTipoEvento.getSelectedItem();
        switch (tipo) {
            case "Concierto":
                guardarConcierto();
                break;
            case "Obra de Teatro":
                guardarObraTeatro();
                break;
            case "Exposición de Arte":
                guardarExposicionArte();
                break;
        }
    }

private void guardarConcierto() throws Exception {
        Concierto concierto = conciertoController.findConcierto(presentacion.getIdPresentacion());
        if (concierto == null) {
            concierto = new Concierto();
            concierto.setIdConcierto(presentacion.getIdPresentacion());
        }
        concierto.setNumArtistas(Integer.parseInt(txtNumArtistas.getText()));
        concierto.setGeneroMusical(txtGeneroMusical.getText());

        if (conciertoController.findConcierto(concierto.getIdConcierto()) == null) {
            conciertoController.create(concierto);
        } else {
            conciertoController.edit(concierto);
        }
    }
 private void guardarObraTeatro() throws Exception {
        Obrateatro obra = obraTeatroController.findObrateatro(presentacion.getIdPresentacion());
        if (obra == null) {
            obra = new Obrateatro();
            obra.setIdObra(presentacion.getIdPresentacion());
        }
        obra.setNumActos(Integer.parseInt(txtNumActos.getText()));

        if (obraTeatroController.findObrateatro(obra.getIdObra()) == null) {
            obraTeatroController.create(obra);
        } else {
            obraTeatroController.edit(obra);
        }
    }
 private void guardarExposicionArte() throws Exception {
        Exposicionarte expo = exposicionArteController.findExposicionarte(presentacion.getIdPresentacion());
        if (expo == null) {
            expo = new Exposicionarte();
            expo.setIdExpo(presentacion.getIdPresentacion());
        }
        expo.setDiasExposicion(Integer.parseInt(txtDiasExposicion.getText()));

        if (exposicionArteController.findExposicionarte(expo.getIdExpo()) == null) {
            exposicionArteController.create(expo);
        } else {
            exposicionArteController.edit(expo);
        }
    }

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try {
            if (presentacion == null) {
                presentacion = new Presentacion();
            }
            llenarDatosPresentacion();
            guardarPresentacion();
            guardarTipoPresentacion();
            JOptionPane.showMessageDialog(this, "Registro guardado correctamente");
            dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al guardar: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

