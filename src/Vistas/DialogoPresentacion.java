    private void initControllers() {
        Persistencia persis = new Persistencia();
        presentacionController = new PresentacionJpaController(persis.getEmf());
        conciertoController = new ConciertoJpaController(persis.getEmf());
        obraTeatroController = new ObrateatroJpaController(persis.getEmf());
        exposicionArteController = new ExposicionarteJpaController(persis.getEmf());
        festivalController = new FestivalJpaController(persis.getEmf());
        escenarioController = new EscenarioJpaController(persis.getEmf());
    }
    
     private void cargarFestivales() {
        cbFestival.removeAllItems();
        List<Festival> festivales = festivalController.findFestivalEntities();
        for (Festival f : festivales) {
            cbFestival.addItem(f);
        }
    }
    
    private void cargarEscenarios() {
        cbEscenario.removeAllItems();
        List<Escenario> escenarios = escenarioController.findEscenarioEntities();
        for (Escenario e : escenarios) {
            cbEscenario.addItem(e);
        }
    }
    
    private void configurarDialogo() {
        if (presentacion != null) {
            txtTitulo.setText(presentacion.getTitulo());
            txtDuracion.setText(String.valueOf(presentacion.getDuracion()));
            txtCostoBase.setText(String.valueOf(presentacion.getCostoBase()));
            cbFestival.setSelectedItem(presentacion.getIdFestival());
            cbEscenario.setSelectedItem(presentacion.getIdEscenario());

            Concierto concierto = conciertoController.findConcierto(presentacion.getIdPresentacion());
            Obrateatro obra = obraTeatroController.findObrateatro(presentacion.getIdPresentacion());
            Exposicionarte exposicion = exposicionArteController.findExposicionarte(presentacion.getIdPresentacion());

            if (concierto != null) {
                cbTipoEvento.setSelectedItem("Concierto");
                txtNumArtistas.setText(String.valueOf(concierto.getNumArtistas()));
                txtGeneroMusical.setText(concierto.getGeneroMusical());
            } else if (obra != null) {
                cbTipoEvento.setSelectedItem("Obra de Teatro");
                txtNumActos.setText(String.valueOf(obra.getNumActos()));
            } else if (exposicion != null) {
                cbTipoEvento.setSelectedItem("Exposición de Arte");
                txtDiasExposicion.setText(String.valueOf(exposicion.getDiasExposicion()));
            }
        } else {
            cbTipoEvento.setSelectedIndex(0);
        }
        cambiarPanelEspecifico();
    }
    private void cambiarPanelEspecifico() {
        pnlConcierto.setVisible(false);
        pnlObraTeatro.setVisible(false);
        pnlExposicion.setVisible(false);

        String tipo = (String) cbTipoEvento.getSelectedItem();
        switch (tipo) {
            case "Concierto":
                pnlConcierto.setVisible(true);
                break;
            case "Obra de Teatro":
                pnlObraTeatro.setVisible(true);
                break;
            case "Exposición de Arte":
                pnlExposicion.setVisible(true);
                break;
        }
    }
