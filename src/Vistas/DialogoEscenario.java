    private void btnGuardar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardar1ActionPerformed
        try {
            if (txtNombre.getText().isEmpty() || txtCapacidad.getText().isEmpty() || txtUbicacion.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Por favor, complete todos los campos");
                return;
            }
            
            if (escenario == null) {
                escenario = new Escenario();
            }
            escenario.setNombre(txtNombre.getText());
            escenario.setCapacidad(Integer.parseInt(txtCapacidad.getText()));
            escenario.setUbicacion(txtUbicacion.getText());
            
            if (escenario.getIdEscenario() == null) {
                escenarioController.create(escenario);
            } else {
                escenarioController.edit(escenario);
            }
            
            JOptionPane.showMessageDialog(this, "Escenario guardado correctamente");
            dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al guardar: " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnGuardar1ActionPerformed

